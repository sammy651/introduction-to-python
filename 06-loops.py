# Loops in Python

# Loops are used to perform repetitive tasks.
# There are 3 types of loops in Python
# 1. For loops
# 2. While loops
# 3. Range loops

# 1. For loops
# =================================================================
# The for loop is used to iterate over a sequence (list, tuple, string, etc.).

# Example
for x in range(5):
    print(x)

# 2. While loops
# =================================================================
# The while loop is used to iterate over a sequence (list, tuple, string, etc.).

# Example
x = 0
while x < 5:
    print(x)
    
    if x == 3:
        continue
    x += 1
    
